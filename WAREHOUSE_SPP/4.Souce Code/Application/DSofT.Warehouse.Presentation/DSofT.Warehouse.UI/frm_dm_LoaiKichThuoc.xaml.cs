                using (var task = new AsyncTaskProcess(this))
                {
                    ServiceAction<DataTable> action = ((pd) =>
                    {
                        task.SetCallContext();
                        return _DM_LOAI_KTHUOCBussiness.GetListDM_LOAI_KTHUOC(ConstCommon.DonViQuanLy);
                    });
